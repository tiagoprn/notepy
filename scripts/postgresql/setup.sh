source host-postgres.env
source ../../notepy.env

NEWUSERPASSWORD=$PGPASSWORD
PGPASSWORD="$HOST_PGPASSWORD" psql -h 127.0.0.1 -p 5999 -U $HOST_POSTGRES_USER -c "CREATE USER $POSTGRES_USER WITH PASSWORD '$NEWUSERPASSWORD';"
PGPASSWORD="$HOST_PGPASSWORD" psql -h 127.0.0.1 -p 5999 -U $HOST_POSTGRES_USER -c "CREATE DATABASE $POSTGRES_DB WITH OWNER = '$POSTGRES_USER';"
