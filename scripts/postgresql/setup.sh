PGPASSWORD="$HOST_PGPASSWORD" psql -h 127.0.0.1 -p 5999 -U $HOST_POSTGRES_USER -c "ALTER ROLE $POSTGRES_USER SET default_transaction_isolation TO 'read committed';"
PGPASSWORD="$HOST_PGPASSWORD" psql -h 127.0.0.1 -p 5999 -U $HOST_POSTGRES_USER -c "ALTER ROLE $POSTGRES_USER SET timezone TO 'UTC';"
PGPASSWORD="$HOST_PGPASSWORD" psql -h 127.0.0.1 -p 5999 -U $HOST_POSTGRES_USER -c "GRANT ALL PRIVILEGES ON DATABASE $POSTGRES_DB TO $POSTGRES_USER;"

